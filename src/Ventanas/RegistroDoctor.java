    private void jb_regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_regActionPerformed
        String ced = jtf_ced.getText();
        String nom = jtf_nom.getText();
        String ape = jtf_ape.getText();
        String esp = jtf_esp.getText();
        String dir = jtf_dir.getText();
        String sal = jtf_sal.getText();
        String tel = jtf_tel.getText();

        if (ced.isEmpty() || nom.isEmpty() || ape.isEmpty() || esp.isEmpty() || dir.isEmpty() || sal.isEmpty() || tel.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Complete todos los espacios", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            jb_mod.setEnabled(true);
            jb_eli.setEnabled(true);
            jtf_bus.setEnabled(true);
            jtf_ced.setEnabled(true);
            if (jb_reg.getText().equals("Registrar")) {
                File nuevoMedico = new File("Hospital\\Medicos\\" + ced + ".txt");
                if (nuevoMedico.exists()) {
                    JOptionPane.showMessageDialog(null, "Medico ya registrado", "ATENCIÓN", JOptionPane.WARNING_MESSAGE);
                } else {
                    try {
                        FileWriter write = new FileWriter("Hospital\\Medicos\\" + ced + ".txt", true);
                        try (PrintWriter print_line = new PrintWriter(write)) {
                            print_line.printf(ced + "\n" + nom + "\n" + ape + "\n" + esp + "\n" + dir + "\n" + sal + "\n" + tel);
                            JOptionPane.showMessageDialog(null, "Medico registrado exitosamente", "EXITO", JOptionPane.INFORMATION_MESSAGE);
                            jtf_ced.setText("");
                            jtf_nom.setText("");
                            jtf_ape.setText("");
                            jtf_esp.setText("");
                            jtf_dir.setText("");
                            jtf_sal.setText("");
                            jtf_tel.setText("");
                        }
                    } catch (IOException ex) {
                        Logger.getLogger(RegistroDoctor.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } else if (jb_reg.getText().equals("Guardar")) {
                File file = new File("Hospital\\Medicos\\" + ced + ".txt");
                file.delete();
                try {
                    FileWriter write = new FileWriter("Hospital\\Medicos\\" + ced + ".txt", true);
                    try (PrintWriter print_line = new PrintWriter(write)) {
                        print_line.printf(ced + "\n" + nom + "\n" + ape + "\n" + esp + "\n" + dir + "\n" + sal + "\n" + tel);
                        jtf_ced.setText("");
                        jtf_nom.setText("");
                        jtf_ape.setText("");
                        jtf_esp.setText("");
                        jtf_dir.setText("");
                        jtf_sal.setText("");
                        jtf_tel.setText("");
                        jtf_bus.setText("");
                        JOptionPane.showMessageDialog(null, "Medico modifica exitosamente", "EXITO", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (IOException ex) {
                    Logger.getLogger(RegistroDoctor.class.getName()).log(Level.SEVERE, null, ex);
                }
                jb_reg.setText("Registrar");
            } else {
                JOptionPane.showMessageDialog(null, "ERROR....");
            }
        }


    }//GEN-LAST:event_jb_regActionPerformed

