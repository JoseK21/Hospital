    private void jb_eliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_eliActionPerformed
        String bus = jtf_bus.getText();
        if (bus.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese cedula del paciente", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            File file = new File("Hospital\\Medicos\\" + bus + ".txt");
            if (file.delete()) {
                JOptionPane.showMessageDialog(null, "Paciente eliminado exitosamente", "EXITO", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Paciente " + bus + " inexistente", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
        jtf_bus.setText("");

    }//GEN-LAST:event_jb_eliActionPerformed

    private void jb_modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_modActionPerformed
        String bus = jtf_bus.getText();
        if (bus.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese cedula del paciente", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {            
            File nuevoPaciente = new File("Hospital\\Medicos\\" + bus + ".txt");
            if (nuevoPaciente.exists()) {
                // Asignación de texto al boton registrar->guardar y la casilla de busqueda de cedula a vacio
                jb_reg.setText("Guardar");
                jtf_bus.setText("");
                // Bloqueo de casiilas y botones
                jb_mod.setEnabled(false);
                jb_eli.setEnabled(false);
                jtf_bus.setEnabled(false);
                jtf_ced.setEnabled(false);

                BufferedReader reader;
                try {
                    reader = new BufferedReader(new FileReader("Hospital\\Medicos\\" + bus + ".txt"));
                    String line = reader.readLine();
                    int numLinea = 1;
                    while (line != null) {
                        if (numLinea == 1) {
                            jtf_ced.setText(line);
                        } else if (numLinea == 2) {
                            jtf_nom.setText(line);
                        } else if (numLinea == 3) {
                            jtf_ape.setText(line);
                        } else if (numLinea == 4) {
                            jtf_esp.setText(line);
                        } else if (numLinea == 5) {
                            jtf_dir.setText(line);
                        } else if (numLinea == 6) {
                            jtf_sal.setText(line);
                        } else if (numLinea == 7) {
                            jtf_tel.setText(line);
                        } else {
                            break;
                        }
                        line = reader.readLine();
                        numLinea++;
                    }
                    reader.close();
                } catch (IOException e) {
                }
            } else {
                JOptionPane.showMessageDialog(null, "Paciente " + bus + " inexistente", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
        jtf_bus.setText("");
    }//GEN-LAST:event_jb_modActionPerformed

